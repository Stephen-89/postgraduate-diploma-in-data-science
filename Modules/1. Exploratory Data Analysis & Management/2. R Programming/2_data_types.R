# 1. Creating a Numeric Object
# Assign a numeric value to an object
x <- 42
# Display the object
print(x)


# 2. Creating a Character Object
# Assign a character string to an object
name <- "John Doe"
# Display the object
print(name)


# 3. Creating a Vector
# Assign a numeric vector to an object
numbers <- c(1, 2, 3, 4, 5)
# Display the object
print(numbers)


# 4. Creating a List
# Create a list object
my_list <- list(name = "Alice", age = 30, is_student = TRUE)
# Display the object
print(my_list)


# 5. Creating a Data Frame
# Create a data frame object
df <- data.frame(Name = c("John", "Alice", "Bob"), Age = c(25, 30, 22), Gender = c("M", "F", "M"))
# Display the object
print(df)


# 6. Creating a Matrix
# Create a matrix object
matrix_obj <- matrix(1:9, nrow = 3, ncol = 3)
# Display the object
print(matrix_obj)